package com.spark.common.utils.excel;

public class DoubleString {

	private String label;
	private String value;

	public DoubleString(String label,String value) {
	 this.label = label;
	 this.value = value;
	}

	public String getLabel() {
		return label;
	}

	public String getValue() {
		return value;
	}

}
